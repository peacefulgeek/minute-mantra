#!/usr/bin/env node
/**
 * Generates seeds/mantras.json and seeds/mantras.sql from the seed data.
 * Run: node scripts/export-seed-files.js
 */
const fs = require('fs');
const path = require('path');

const p1 = require('../seeds/mantras-part1');
const p2 = require('../seeds/mantras-part2');
const p3 = require('../seeds/mantras-part3');
const p4 = require('../seeds/mantras-part4');

const all = [...p1, ...p2, ...p3, ...p4].sort((a, b) => a.day_of_year - b.day_of_year);

// Add default fields
const withDefaults = all.map(m => ({
  ...m,
  audio_filename: `mantra-${String(m.day_of_year).padStart(3, '0')}.mp3`,
  go_deeper_url: 'https://paulwagner.com',
  hero_image_url: null,
  audio_url: null,
}));

// ── JSON export ──────────────────────────────────────────────────────────────
const jsonPath = path.join(__dirname, '../seeds/mantras.json');
fs.writeFileSync(jsonPath, JSON.stringify(withDefaults, null, 2));
console.log(`✅ Written: ${jsonPath} (${withDefaults.length} records)`);

// ── SQL export ───────────────────────────────────────────────────────────────
function esc(val) {
  if (val === null || val === undefined) return 'NULL';
  return "'" + String(val).replace(/'/g, "\\'").replace(/\n/g, '\\n') + "'";
}

const lines = [
  '-- Minute Mantra — 365 Mantra Records',
  '-- Generated by scripts/export-seed-files.js',
  '',
  'INSERT INTO mantras',
  '  (day_of_year, original_script, transliteration, english_translation,',
  '   tradition, intention, phonetic_guide, audio_filename, go_deeper_url,',
  '   go_deeper_teaser, context_note, sacred_geometry_type)',
  'VALUES',
];

const rows = withDefaults.map((m, i) => {
  const comma = i < withDefaults.length - 1 ? ',' : ';';
  return `  (${m.day_of_year}, ${esc(m.original_script)}, ${esc(m.transliteration)}, ${esc(m.english_translation)}, ${esc(m.tradition)}, ${esc(m.intention)}, ${esc(m.phonetic_guide)}, ${esc(m.audio_filename)}, ${esc(m.go_deeper_url)}, ${esc(m.go_deeper_teaser)}, ${esc(m.context_note)}, ${esc(m.sacred_geometry_type)})${comma}`;
});

const sqlContent = [...lines, ...rows].join('\n');
const sqlPath = path.join(__dirname, '../seeds/mantras.sql');
fs.writeFileSync(sqlPath, sqlContent);
console.log(`✅ Written: ${sqlPath}`);

// ── audio-review-failures.txt placeholder ───────────────────────────────────
const failuresPath = path.join(__dirname, '../seeds/audio-review-failures.txt');
if (!fs.existsSync(failuresPath)) {
  fs.writeFileSync(failuresPath, 
    'Audio Review Failures\n' +
    '=====================\n' +
    'This file lists mantra IDs where audio generation failed quality review.\n' +
    'These mantras will show the phonetic_guide text in the app instead of audio.\n\n' +
    'No failures recorded yet. Run scripts/generate-audio.js to populate.\n'
  );
  console.log(`✅ Written: ${failuresPath}`);
}

console.log('\n✅ Seed file export complete!');
